    private void btnCambiarValorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCambiarValorActionPerformed
        int filaSeleccionada = tblPrecios.getSelectedRow();
        if (filaSeleccionada != -1) { // Verificar si se ha seleccionado alguna fila
            // Crear una instancia de UICarteleraElectronica y pasarle el objeto Parking como parámetro
            double nuevaTarifa = Double.parseDouble(txtNuevoValor.getText());
            double devuelto = controlador.actualizarValorTarifa(filaSeleccionada, nuevaTarifa);
            if (devuelto != -1) {
                JOptionPane.showMessageDialog(this, "Valor demasiado alto. El sistema no permite dispersión de precios por encima del 100%. Ingrese un valor menor a 2x" + devuelto);
            }

        } else {
            // Si no se ha seleccionado ninguna fila, mostrar un mensaje de advertencia
            JOptionPane.showMessageDialog(this, "Por favor, seleccione una fila en la grilla.");
        }

    }//GEN-LAST:event_btnCambiarValorActionPerformed

