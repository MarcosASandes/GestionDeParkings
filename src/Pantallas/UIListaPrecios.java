    @Override
    public void mostrarDatosPrecios(Parking parking, ArrayList<Tarifario> tarifas) {
        lblNombreParking.setText("Lista de precios - <" + parking.getNombre() + ">");
        DefaultTableModel model = (DefaultTableModel) tblPrecios.getModel();
        model.setRowCount(0);
        for (Tarifario t : tarifas) {
            Object[] fila = new Object[]{
                t.getTp().getDescripcion(),
                t.getTarifa()
            };
            model.addRow(fila);
        }
    }
