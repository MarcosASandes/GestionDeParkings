    private void btnIngresoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresoActionPerformed
        int vehiculoSeleccionado = tblVehiculo.getSelectedRow();
        int cocheraSeleccionada = tblCochera.getSelectedRow();

        if (vehiculoSeleccionado != -1 && cocheraSeleccionada != -1) { // Verificar si se ha seleccionado alguna fila
            // Crear una instancia de UICarteleraElectronica y pasarle el objeto Parking como parámetro
            controlador.cargarIngreso(vehiculoSeleccionado, cocheraSeleccionada);
        } else {
            // Si no se ha seleccionado ninguna fila, mostrar un mensaje de advertencia
            JOptionPane.showMessageDialog(this, "Por favor, seleccione una fila en la grilla.");
        }
    }//GEN-LAST:event_btnIngresoActionPerformed

