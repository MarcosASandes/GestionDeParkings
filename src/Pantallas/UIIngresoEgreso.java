    @Override
    public void mostrarVehiculosCocheras(ArrayList<Estacionable> cocheras, ArrayList<Transitable> vehiculos) {
// Obtener los modelos de tabla existentes
    DefaultTableModel modeloVehiculos = (DefaultTableModel) tblVehiculo.getModel();
    DefaultTableModel modeloCocheras = (DefaultTableModel) tblCochera.getModel();

    // Limpiar las tablas antes de agregar los nuevos datos
    modeloVehiculos.setRowCount(0);
    modeloCocheras.setRowCount(0);

    // Llenar tabla de vehículos
    for (Transitable vehiculo : vehiculos) {
        Object[] fila = {
            vehiculo.getPatente()
        };
        modeloVehiculos.addRow(fila);
    }

    // Llenar tabla de cocheras
    for (Estacionable cochera : cocheras) {
        Object[] fila = {
            cochera.getCodigo()
        };
        modeloCocheras.addRow(fila);
    }
    }

